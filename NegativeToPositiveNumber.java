public class NegativeToPositiveNumber {

	public static void main(String[] args) {
	int numb = -40;
	if(numb<0) {
		System.out.println("Given number " + numb + " is negative number");
		int res = -(numb);
		System.out.println("The number " + numb + " is converted into " + res);
	}
		else
			System.out.println("The number" + numb + " is positive number");
	
		
	
	

	}

}
